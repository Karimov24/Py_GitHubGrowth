#Modulo!!
#Python offers a companion to the division operator called the modulo operator. The modulo operator is 
# indicated by % and gives the remainder of a division calculation. If the two numbers are divisible, 
# then the result of the modulo operation will be 0.
#----------------------------------------------------

#every 11th customer gets 11% off of their next order.
order_263_r = 263 % 11
print(order_263_r)

order_263_coupon = "No"

order_264_r = 264 % 11
print(order_264_r)

order_264_coupon = "Yes"
